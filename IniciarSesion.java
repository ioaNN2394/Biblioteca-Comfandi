    private void btnVerMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerMenuActionPerformed
    
    String busqueda_usuario = metodo.buscarUsuarioRegistrado(txtUSUARIO.getText(),txtCONTRA.getText());
       
       if(txtUSUARIO.getText().equals("root")&& txtCONTRA.getText().equals("root")){
           JOptionPane.showMessageDialog(this,"iniciaste sesion como root \n(administrador)");
        HOME entrada = new HOME();
        entrada.setVisible(true);
        this.setVisible(false);
        entrada.lblNOMBRE.setText("Administrador");
        
        this.dispose();
       
       }else if (busqueda_usuario.equals("usuario encontrado")){
           String busqueda_nombre = metodo.buscarnombre(txtUSUARIO.getText());
          JOptionPane.showMessageDialog(this, "Bienvenido(a) \n"+busqueda_nombre);
           //\
            HOME entrada = new HOME();
        entrada.setVisible(true);
        this.setVisible(false);
        entrada.lblNOMBRE.setText(busqueda_nombre);
        this.dispose();
       }else{
             JOptionPane.showMessageDialog(this, "Usuario no registrado, porfavor registrese");
       }
       
    }//GEN-LAST:event_btnVerMenuActionPerformed

